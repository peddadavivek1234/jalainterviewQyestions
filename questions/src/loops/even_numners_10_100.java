package loops;

public class even_numners_10_100 {
	public static void main(String[] args) {

        System.out.println("Even Numbers between 10 and 100:");
        int num = 10;
        // Loop to print even numbers between 10 and 100
        while (num <= 100) {
            System.out.println(num);
            num += 2;
        }
    }
}
