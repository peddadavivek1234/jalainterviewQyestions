package jala_questions;

public class relational_operator {
	public static void main(String[] args) {
		int x = 10, y = 5;

        System.out.println("x < y: " + (x < y));
        System.out.println("x <= y: " + (x <= y));
        System.out.println("x > y: " + (x > y));
        System.out.println("x >= y: " + (x >= y));
	}
}
