package jala_questions;

public class equal_notequal_operator {
	public static void main(String[] args) {
		 int x = 10, y = 5;
	     int z = 10;
	     System.out.println("x == y: " + (x == y));
	     System.out.println("x == z: " + (x == z));
	     System.out.println("x != y: " + (x != y));
	     System.out.println("x != z: " + (x != z));
	}
}
